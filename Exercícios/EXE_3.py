#Programa para ler dois números e mostrar a soma dos valores
n1 = int(input("Digite um primeiro número: ")) #Variável para armazenar o primeiro número
n2 = int(input("Digite um segundo número: ")) #Variável para armazenar o segundo número
soma = n1 + n2 #Variável para armazenar a soma dos valores
print(f"A soma entre {n1} e {n2}: {soma}") #Mostrar os valores na tela.
